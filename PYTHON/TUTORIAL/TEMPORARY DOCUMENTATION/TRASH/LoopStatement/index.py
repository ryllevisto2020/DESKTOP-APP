#for loop
for item in range(10): #=> range(amount)
    print(item)

print("================================================")

#for loop with start and end
for item in range(1,10): #=> range(start,end)
    print(item)

print("================================================")

#for loop with start,end and step
for item in range(1,10,2): #=> range(start,end,step)
    print(item)

print("================================================")

#While loop
i = 1
while i < 10:
    print(i)
    i = i + 1 